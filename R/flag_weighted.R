#' Flag aggregation by the weighted frequency method
#' @description This method can be used when you want to derive the flag of an aggregate that is a weighted average,
#' index, quantile, etc.
#' @param i An integer column identifier of data.frame or a matrix containing the flags and weights used to derived
#' the flag for the aggregates.
#' @param f A data.frame or a matrix containing the flags of the series (one column per period)
#' @param w A data.frame or a matrix with same size and dimesion as \code{f} containing the corresponding weights
#'  for each flags.
#' @return \code{flag_weighted} Returns a caracter vector with the flag that has the highest weighted frequency as the first
#' value, and the sum of weights for the given flag as the second value for the given columns of \code{f,w} defined
#' by the parameter \code{i}.
#'
#' @seealso \code{\link{flag_divide}}
#' @examples
#' \dontrun{
#' library(tidyr)
#' flags <- spread(test_data[, c(1:3)], key = time, value = flags)
#' weights <- spread(test_data[, c(1, 3:4)], key = time, value = values)
#' flag_weighted(7,flags[, c(2:ncol(flags))],weights[, c(2:ncol(weights))])
#'
#' weights<-apply(weights[, c(2:ncol(weights))],2,function(x) x/sum(x,na.rm=T))
#' weights[is.na(weights)] <- 0
#' flags<-flags[, c(2:ncol(flags))]
#' sapply(1:ncol(flags),flag_weighted,f=flags,w=weights)
#'}
#' @export
#' @importFrom stats aggregate

flag_weighted<-function(i,f,w){
  inp<-as.data.frame(cbind(f[,i],w[,i]),stringsAsFactors = F)
  inp<-inp[!is.na(inp[,1]),]
  out<-c()
  if(any(nchar(inp$V1)>1)){
    tmp1<-do.call(rbind, apply(inp,1,flag_divide))
  }
  else{
    tmp1=inp
  }
  rownames(tmp1)<-c()
  tmp1<-as.data.frame(tmp1)
  tmp1$V2<-as.numeric(as.character(tmp1$V2))
  w_sum<-aggregate(tmp1$V2,by=list(tmp1$V1),FUN=sum) #  aggregate(V2~V1,tmp1,sum)
  c(as.character(w_sum[order(-w_sum$x),][1,1]), as.numeric(w_sum[order(-w_sum$x),][1,2]))
}
